import * as crypto from "crypto";
import * as fs from "fs";
import * as os from "os";
import * as path from "path";

// MVP1: 実 Claude Code セッション (.jsonl) を読み、user レベルの Fork 木を
// ノードモデル（描画に必要な最小フィールド）へ変換する。
// 設計根拠は PROTOTYPES.md §6.4（S0 調査結果）を参照。

export interface GraphNode {
  id: string; // = レコードの uuid
  // メモ・ピンの永続キー（MVP4）。canonical key（timestamp+type+内容）の SHA-1 短縮ハッシュ。
  // id（canonical uuid）は「最初に読んだ個体」で決まりファイルの読み込み順に依存して変わり得るが、
  // canonical key は論理イベントの同一性そのものなので再読込・Fork 複製をまたいで安定する。
  key: string;
  parent: string | null; // = 最も近い user 祖先の uuid（無ければ null = ルート）
  lane: number; // 描画レーン（MVP1 は素朴割当。原則A/B は MVP2）
  prompt: string; // message.content のテキスト
  session?: string; // 所属セッション（= rec.sessionId）。MVP3 の再開で使う
  timestamp?: string; // 発話時刻（ISO 8601）。右パネルの「いつ」表示に使う（MVP6）
  memo?: string; // 保存済みメモ（拡張本体が globalState から付与。MVP4）
  pinned?: boolean; // ピン留め（同上）
  // 親からこのノードへの辺の途中で /compact（コンテキスト圧縮）が実行された印。
  // Webview は該当エッジに 2 重波線（チャートの省略記号 ≈）を重ねて描く
  compacted?: boolean;
}

// 1行ぶんの JSONL レコード（必要なものだけ緩く型付け）
interface RawRecord {
  type?: string;
  uuid?: string;
  parentUuid?: string | null;
  sessionId?: string;
  // ISO 8601。Fork で uuid/sessionId は振り直されるが timestamp は保存される（S1 §2）。
  // 例外: fork 地点の末端 1 件だけは Fork 実行時刻に再スタンプされる（S1 §10.2）→ 二次結合で補修
  timestamp?: string;
  message?: { content?: unknown };
  attachment?: unknown; // attachment レコードの本体（指紋計算に使う）
  subtype?: string; // system レコードの subtype（指紋計算に使う）
  promptId?: string; // user: 投入プロンプトの安定 ID。Fork コピー後も保存される（S1 §10.3）
  requestId?: string; // assistant: API 呼び出しの安定 ID。同上（1ターン複数行で共有される点に注意）
  // true = ハーネスのシステム挿入（人の発話ではない）。代表例は VS Code がセッション再開時に注入する
  // 「Continue from where you left off.」。テキストでは判別できないためフラグで除外する（JSONL-FORMAT.md §2.1）
  isMeta?: boolean;
  // system/compact_boundary レコードのみ: 圧縮直前の末尾レコードの uuid。
  // compact_boundary は parentUuid が null（鎖がここで切れる）ため、これで橋渡ししないと
  // /compact 以降のノードがルートに化けて別の木になってしまう
  logicalParentUuid?: string | null;
}

// Claude Code のプロジェクトフォルダ名はパスの英数字以外を "-" に置換したもの。
// 例: c:\Users\natum\Desktop\claude-app\fork-graph
//   → c--Users-natum-Desktop-claude-app-fork-graph
export function encodeProjectPath(fsPath: string): string {
  return fsPath.replace(/[^A-Za-z0-9]/g, "-");
}

function projectsRoot(): string {
  return path.join(os.homedir(), ".claude", "projects");
}

// 与えられたワークスペースパスに対応する projects/<encoded> ディレクトリを返す。
// 完全一致が無ければ大文字小文字無視で探す（ドライブレターの揺れ対策）。
// export は「表示中フォルダ ≠ ワークスペースのフォルダ」判定（別フォルダのチャットは CLI で開く）に使う。
export function resolveProjectDir(workspacePath: string): string | undefined {
  const root = projectsRoot();
  if (!fs.existsSync(root)) {
    return undefined;
  }
  const encoded = encodeProjectPath(workspacePath);
  const exact = path.join(root, encoded);
  if (fs.existsSync(exact)) {
    return exact;
  }
  const lower = encoded.toLowerCase();
  for (const name of fs.readdirSync(root)) {
    if (name.toLowerCase() === lower) {
      return path.join(root, name);
    }
  }
  return undefined;
}

// プロジェクトフォルダ内で最後に更新された .jsonl（＝直近のセッション）を選ぶ。
function latestSessionFile(projectDir: string): string | undefined {
  const files = fs
    .readdirSync(projectDir)
    .filter((f) => f.endsWith(".jsonl"))
    .map((f) => {
      const full = path.join(projectDir, f);
      return { full, mtime: fs.statSync(full).mtimeMs };
    })
    .sort((a, b) => b.mtime - a.mtime);
  return files[0]?.full;
}

// レコードの message.content から「人の発話」と見なせる text ブロックを集める。
// 重要: 1 つの user レコードに <ide_opened_file> 等の自動添付ブロックと本文ブロックが
// 同居することがある（例: [ <ide_opened_file>…, "うまくいきました。一気に" ]）。
// 先頭ブロックだけ見てレコード単位でノイズ判定すると本物のプロンプトを取りこぼすため、
// 必ずブロック単位で判定する（2026-06-11 修正。それまで実プロンプト多数が欠落していた）。
function genuineTextBlocks(rec: RawRecord): string[] {
  const c = rec.message?.content;
  if (typeof c === "string") {
    return isHarnessNoise(c) ? [] : [c];
  }
  const out: string[] = [];
  if (Array.isArray(c)) {
    for (const block of c) {
      if (block && typeof block === "object") {
        const b = block as { type?: string; text?: string };
        if (typeof b.text === "string" && !isHarnessNoise(b.text)) {
          out.push(b.text);
        }
      }
    }
  }
  return out;
}

// ハーネス（CLI/IDE）が自動挿入したラッパー文字列で始まるテキストは、
// 「ユーザーが打った言葉」ではないのでノードから除外する。
// 例: <ide_opened_file> / スラッシュコマンド / /compact の各種出力 / 中断通知 / 継続サマリ。
const NOISE_PREFIXES = [
  "<ide_opened_file>",
  "<ide_selection>",
  "<command-name>",
  "<command-message>",
  "<command-args>",
  "<local-command-caveat>",
  "<local-command-stdout>",
  "<local-command-stderr>",
  "<system-reminder>",
  "[Request interrupted",
  "Caveat: The messages below were generated by the user",
  "This session is being continued from a previous conversation",
];

function isHarnessNoise(text: string): boolean {
  const t = text.trimStart();
  if (t === "") {
    return true;
  }
  return NOISE_PREFIXES.some((p) => t.startsWith(p));
}

// content が tool_result だけ（＝ツール出力の差し戻し、ユーザーの発話ではない）かを判定。
function isOnlyToolResult(rec: RawRecord): boolean {
  const c = rec.message?.content;
  if (!Array.isArray(c) || c.length === 0) {
    return false;
  }
  return c.every(
    (b) => b && typeof b === "object" && (b as { type?: string }).type === "tool_result"
  );
}

